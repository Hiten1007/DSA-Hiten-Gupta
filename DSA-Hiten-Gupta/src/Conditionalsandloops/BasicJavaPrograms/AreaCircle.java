import java.util.*;

public class AreaCircle {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the radius of circle here : ");
        int r = sc.nextInt();
        System.out.print("The area of circle is : " + 3.14 * r * r);
    }
}
